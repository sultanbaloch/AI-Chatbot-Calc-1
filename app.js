function plus() {
    let num1 = document.getElementById("num1").value;
    let num2 = document.getElementById("num2").value;

    document.getElementById("result").innerHTML = Number (num1) + Number (num2)
}

function minus() {
    let num1 = document.getElementById("num1").value;
    let num2 = document.getElementById("num2").value;

    document.getElementById("result").innerHTML = Number (num1) - Number (num2)
}

function multiply() {
    let num1 = document.getElementById("num1").value;
    let num2 = document.getElementById("num2").value;

    document.getElementById("result").innerHTML = Number (num1) * Number (num2)
}
function divide() {

    let num1 = document.getElementById("num1").value;
    let num2 = document.getElementById("num2").value;

    document.getElementById("result").innerHTML = Number (num1) / Number (num2)
}
